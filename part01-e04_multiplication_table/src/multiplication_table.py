#!/usr/bin/env python3


def main():
    for i in range(11):
        if i == 0:
            next
        else:
            help(i)


def help(num):
    for i in range(11):
        if i == 0:
            next
        else:
            print(num*i, end=' ')
    print("")


if __name__ == "__main__":
    main()
